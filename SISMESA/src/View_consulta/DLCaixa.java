 private void PreencherJtable() {
        if (TFConsulta.getText().isEmpty()) {
            rsConsultaCaixa = FMaster.con.executeSQL("select * from cxacaixa");
        } else {
            rsConsultaCaixa = FMaster.con.executeSQL("select * from cxacaixa  where UPPER(descricao) like UPPER('%" + TFConsulta.getText() + "%')");
        }
        DefaultTableModel modelo = (DefaultTableModel) TBCaixa.getModel();
        modelo.setNumRows(0);
        try {
            while (rsConsultaCaixa.next()) {
                String situacao = "";
                if (rsConsultaCaixa.getInt("situacao")==0 || rsConsultaCaixa.getInt("situacao")==1){
                    situacao = "Aberto";
                }
                else situacao = "Fechado";
                modelo.addRow(new Object[]{rsConsultaCaixa.getRow(), rsConsultaCaixa.getInt("id_caixa"), rsConsultaCaixa.getString("descricao"), situacao});
            }
        } catch (SQLException ex) {
            Logger.getLogger(DLProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
