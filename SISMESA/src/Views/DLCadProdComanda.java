    private void SalvaProduto() {
        String produt;
        Float vlr_unit_produto = null;
        int qtde;

        ResultSet rsProdComanda = FMaster.con.executeSQL("select * from atncompro where id_comanda = '" + comanda + "'");


        try {
            rsProdComanda.first();
            produt = TFProduto.getText().trim().isEmpty() ? "0" : TFProduto.getText().trim();
            ResultSet rsProduto = FMaster.con.executeSQL("select * from estproduto where id_produto = '" + produt + "'");
            rsProduto.first();
            if (rsProduto.isFirst()) {
                vlr_unit_produto = rsProduto.getFloat("vlr_unitario");
                if (vlr_unit_produto == 0) {
                     JOptionPane.showMessageDialog(null, "Produto não possui valor unitario em seu cadastro!", "Alerta", JOptionPane.ERROR_MESSAGE);
                     rsProduto.close();
                     rsProdComanda.close();
                     return;
                }
                rsProdComanda.moveToInsertRow();
                rsProdComanda.updateInt("id_comanda", comanda);
                rsProdComanda.updateInt("id_com_clie", cliente);
                rsProdComanda.updateInt("id_com_pro", Utilitario.getSequencia("atncompro", "id_com_pro"));
                rsProdComanda.updateInt("id_produto", Integer.parseInt(produt));
                rsProdComanda.updateInt("qtde", Integer.parseInt(TFQtde.getText().isEmpty() ? "0" : TFQtde.getText()));
                rsProdComanda.updateFloat("vlr_unitario", vlr_unit_produto);
                qtde = Integer.parseInt(TFQtde.getText().isEmpty() ? "0" : TFQtde.getText());
                rsProdComanda.updateFloat("vlr_total", (qtde * vlr_unit_produto));
                rsProdComanda.updateFloat("vlr_a_pagar", (qtde * vlr_unit_produto));
                rsProdComanda.updateInt("qtde_a_pagar", qtde);
                rsProdComanda.updateString("observacao", TAObservacao.getText().isEmpty() ? "" : TAObservacao.getText());
                rsProdComanda.insertRow();

                TFProduto.setText("");
                TFDescricao.setText("");
                TFQtde.setText("");
                TAObservacao.setText("");
                TFProduto.requestFocus();

                FrameComanda.CalculaTotaisDoCliente();
                FrameComanda.SetValuesRowTableCliente();
            }

        } catch (SQLException ex) {
            Logger.getLogger(DLCadProdComanda.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                rsProdComanda.close();
            } catch (SQLException ex) {
                Logger.getLogger(DLCadProdComanda.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void PreencheDadosTela() {
        ResultSet rsConsultaProduto = FMaster.con.executeSQL("select * from atncompro where id_comanda = '" + comanda + "' and "
                + "id_com_clie = '" + cliente + "' and id_com_pro = '" + id_com_pro + "'");
        try {
            rsConsultaProduto.first();
            if (rsConsultaProduto.isFirst()) {
                TFProduto.setText(rsConsultaProduto.getString("id_produto"));
                TFQtde.setText(rsConsultaProduto.getString("qtde"));
                TAObservacao.setText(rsConsultaProduto.getString("observacao"));
                FrameComanda.CalculaTotaisDoCliente();
            }


        } catch (SQLException ex) {
            Logger.getLogger(DLCadProdComanda.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private int AlteraProduto() {
        String produt;
        float vlr_unit_produto = 0;
        int qtde;
        ResultSet rsAlteraProduto = FMaster.con.executeSQL("select * from atncompro where id_comanda = '" + comanda + "' and "
                + "id_com_clie = '" + cliente + "' and id_com_pro = '" + id_com_pro + "'");
        try {
            rsAlteraProduto.first();
            if (rsAlteraProduto.isFirst()) {
                if (rsAlteraProduto.getFloat("vlr_total") == rsAlteraProduto.getFloat("vlr_pago")) {
                    JOptionPane.showMessageDialog(null, "Produto pago não pode ser alterado!", "Alerta", JOptionPane.ERROR_MESSAGE);
                    return 1;
                }
                produt = TFProduto.getText().trim().isEmpty() ? "0" : TFProduto.getText().trim();
                ResultSet rsProduto = FMaster.con.executeSQL("select * from estproduto where id_produto = '" + produt + "'");
                rsProduto.first();
                if (rsProduto.isFirst()) {
                    vlr_unit_produto = rsProduto.getFloat("vlr_unitario");
                }
                rsAlteraProduto.updateInt("id_produto", Integer.parseInt(TFProduto.getText().isEmpty() ? "0" : TFProduto.getText()));
                rsAlteraProduto.updateInt("qtde", Integer.parseInt(TFQtde.getText()));
                qtde = Integer.parseInt(TFQtde.getText().isEmpty() ? "0" : TFQtde.getText());
                rsAlteraProduto.updateFloat("vlr_total", (qtde * vlr_unit_produto));
                rsAlteraProduto.updateString("observacao", TAObservacao.getText());
                rsAlteraProduto.updateFloat("vlr_a_pagar", rsAlteraProduto.getFloat("vlr_total") - rsAlteraProduto.getFloat("vlr_pago"));
                rsAlteraProduto.updateInt("qtde_a_pagar", qtde - rsAlteraProduto.getInt("qtde_pago"));
                rsAlteraProduto.updateRow();
                FrameComanda.CalculaTotaisDoCliente();
                FrameComanda.SetValuesRowTableCliente();
                rsProduto.close();
                rsAlteraProduto.close();
                dispose();
            }

        } catch (SQLException ex) {
            Logger.getLogger(DLCadProdComanda.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0;
    }

    private void PosicionaProduto() {
        if (!TFProduto.getText().isEmpty()) {
            ResultSet rsProduto = FMaster.con.executeSQL("select * from estproduto where id_produto = '" + TFProduto.getText() + "'");
            try {
                rsProduto.first();
                if (rsProduto.isFirst()) {
                    TFDescricao.setText(rsProduto.getString("descricao"));
                } else {
                    JOptionPane.showMessageDialog(null, "Produto não cadastrado!", "Alerta", JOptionPane.ERROR_MESSAGE);
                    TFDescricao.setText("");
                }
                rsProduto.close();
            } catch (SQLException ex) {
                Logger.getLogger(DLCadProdComanda.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
