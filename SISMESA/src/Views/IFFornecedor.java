    private void SalvarFornecedor() {
        ResultSet rsFornecedor = FMaster.con.executeSQL("select * from estfornecedor where id_fornecedor = 0");
        try {
            rsFornecedor.first();
            rsFornecedor.moveToInsertRow();
            rsFornecedor.updateInt("id_fornecedor", Utilitario.getSequencia("estfornecedor", "id_fornecedor"));
            rsFornecedor.updateString("nome", TFNome.getText());
            rsFornecedor.updateString("cpf", TFCPF.getText().replace(".", "").replace("-", ""));
            rsFornecedor.updateString("cnpj", TFCNPJ.getText().replace(".", "").replace("-", ""));
            rsFornecedor.updateString("cep", TFCep1.getText().replace(".", "").replace("-", ""));
            rsFornecedor.updateString("cidade", TFCidade.getText());
            rsFornecedor.updateString("uf", CBEstado.getSelectedItem().toString());
            rsFornecedor.updateString("bairro", TFBairro.getText());
            rsFornecedor.updateString("rua", TFCNPJ.getText());
            rsFornecedor.updateInt("numero", Integer.parseInt(TFNumero.getText().isEmpty() ? "0" : TFNumero.getText()));
            rsFornecedor.updateString("obs", TAObs.getText());
            rsFornecedor.updateString("telefone1", TFTelefone01.getText().isEmpty()?"":TFTelefone01.getText().replace("(", "").replace(")", "").replace("-", ""));
            rsFornecedor.updateString("telefone2", TFTelefone02.getText().isEmpty()?"":TFTelefone02.getText().replace("(", "").replace(")", "").replace("-", ""));
            rsFornecedor.insertRow();
            prencherHeder(rsFornecedor.getInt("id_fornecedor"));
            rsFornecedor.close();
            PreencherJtableConsulta();
        } catch (SQLException ex) {
            Logger.getLogger(IFFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void AlterarFornecedor() {
        ResultSet rsFornecedor = FMaster.con.executeSQL("select * from estfornecedor where id_fornecedor  = '" + TFcodCli.getText() + "'");
        try {
            rsFornecedor.first();
            if (rsFornecedor.isFirst()) {
                int iresp = JOptionPane.showOptionDialog(null, "Deseja alterar?", "", JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE,
                        null, //do not use a custom Icon
                        options, //the titles of buttons
                        options[0]); //default button title
                if (iresp == 0) {

                    rsFornecedor.absolute(rsFornecedor.getRow());
                    rsFornecedor.updateString("nome", TFNome.getText());
                    rsFornecedor.updateString("cpf", TFCPF.getText().replace(".", "").replace("-", ""));
                    rsFornecedor.updateString("cnpj", TFCNPJ.getText().replace(".", "").replace("-", ""));
                    rsFornecedor.updateString("cep", TFCep1.getText().replace(".", "").replace("-", ""));
                    rsFornecedor.updateString("cidade", TFCidade.getText());
                    rsFornecedor.updateString("uf", CBEstado.getSelectedItem().toString());
                    rsFornecedor.updateString("bairro", TFBairro.getText());
                    rsFornecedor.updateString("rua", TFCNPJ.getText());
                    rsFornecedor.updateInt("numero", Integer.parseInt(TFNumero.getText().isEmpty() ? "0" : TFNumero.getText()));
                    rsFornecedor.updateString("obs", TAObs.getText());
                    rsFornecedor.updateString("telefone1", TFTelefone01.getText().isEmpty()?"":TFTelefone01.getText().replace("(", "").replace(")", "").replace("-", ""));
                    rsFornecedor.updateString("telefone2", TFTelefone02.getText().isEmpty()?"":TFTelefone02.getText().replace("(", "").replace(")", "").replace("-", ""));
                    rsFornecedor.updateRow();
                    prencherHeder(rsFornecedor.getInt("id_fornecedor"));
                    PreencherJtableConsulta();
                }
            }
            rsFornecedor.close();
        } catch (SQLException ex) {
            Logger.getLogger(IFProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

