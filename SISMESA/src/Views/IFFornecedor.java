    private void DeletaCliente() {

        //
        if (!TFcodCli.getText().isEmpty()) {
            String id_fornecedor = TFcodCli.getText().isEmpty() ? "0" : TFcodCli.getText();
            ResultSet rsFornecedor = FMaster.con.executeSQL("select * from estfornecedor where id_fornecedor = '" + id_fornecedor + "'");
            try {
                rsFornecedor.first();
                if (rsFornecedor.isFirst()) {
                    rsFornecedor.absolute(rsFornecedor.getRow());
                    rsFornecedor.deleteRow();
                    LimpaCampos();
                    PreencherJtableConsulta();
                } else {
                    JOptionPane.showMessageDialog(null, "Um forncedor deve ser informada!", "Alerta", JOptionPane.ERROR_MESSAGE);
                }
                rsFornecedor.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Não foi possível excluir o o fornecedor!", "Alerta", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void PreencherJtableConsulta() {
        ResultSet rsCliente = FMaster.con.executeSQL("select * from estfornecedor " + Filtra() + " order by id_fornecedor");
        DefaultTableModel modelo = (DefaultTableModel) TbPadrao.getModel();
        modelo.setNumRows(0);
        try {
            while (rsCliente.next()) {
                modelo.addRow(new Object[]{rsCliente.getInt("id_fornecedor"), rsCliente.getString("nome"), Utilitario.FormataFone(rsCliente.getString("telefone1")),Utilitario.FormataFone(rsCliente.getString("telefone2")),rsCliente.getString("rua"), rsCliente.getInt("numero"), rsCliente.getString("bairro")});
            }
            rsCliente.close();
        } catch (SQLException ex) {
            Logger.getLogger(IFProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private String Filtra() {

        String descricao = TFDESCGERAL.getText().isEmpty() ? "" : TFDESCGERAL.getText();
        String filtro = "";
        if (!descricao.equals("")) {
            if (filtro.equals("")) {
                filtro = " where Upper(nome) like '%" + descricao.toUpperCase() + "%'";
            } else {
                filtro = filtro + " and Upper(nome) like '%" + descricao.toUpperCase() + "%'";
            }
        }
        return filtro;
    }

